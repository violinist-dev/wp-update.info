    <h3>参加条件</h3>
    <ul>
      <li>古いWordPressはおそらくサーバーも古いので新しいサーバーに移行作業ができる。</li>
      <li>その後のアップデートも続けられる。</li>
      <li>適切な料金を設定できる。異常な安売りがこの状況を招いてる。月数千円で制作会社に任せてたWordPressを引き継ぐと実際には何もやってない詐欺ということがほとんど。制作会社視点で考えてもそんな額でまともな対応できるわけがない当たり前の話。</li>
    </ul>

    <h3>参加方法</h3>
    <ul>
      <li>
        WordPressのユーザー機能を使っているので普通に<a href="/login/?action=register" class="btn btn-outline-dark"
                                      rel="nofollow">ユーザー登録</a>後プロフィールを設定。
      </li>
      <li>参加者リストに表示を有効にするまでは表示されない。使用するのは表示名と下のほうの追加項目。</li>
    </ul>

    <h3>アップデートの申し込み方法</h3>
    <ul>
      <li>対象は「制作会社に任せてたので分からない」「更新して動かなくなるのが怖い」「更新できないように設定されている」ような人です。</li>
      <li>制作会社のサポートを受けているなら更新するように問い合わせましょう。更新もやってくれないような会社なら切り捨てて他の会社を探しましょう。</li>
      <li>各参加者のページから申し込み方法を確認して直接申し込んでください。</li>
    </ul>

